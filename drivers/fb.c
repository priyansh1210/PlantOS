void fb_init(void) {
    /* Look for Bochs VGA adapter */
    struct pci_device *vga = pci_find_device(0x1234, 0x1111);
    if (!vga) {
        kprintf("[FB] No Bochs VGA adapter found\n");
        return;
    }

    /* Read BAR0 — framebuffer base address */
    uint64_t bar0 = vga->bar[0] & ~0xFULL;
    if (bar0 == 0) {
        kprintf("[FB] VGA BAR0 is zero\n");
        return;
    }

    lfb = (uint32_t *)bar0;

    /* Check VBE ID */
    uint16_t id = vbe_read(VBE_DISPI_INDEX_ID);
    if (id < 0xB0C0) {
        kprintf("[FB] VBE ID 0x%04x too old\n", id);
        return;
    }

    /* Mark LFB region as uncacheable (PCD) in boot page tables.
     * Without this, WB-cached writes never reach the VGA device
     * because we boot with -kernel (no BIOS → no MTRRs configured). */
    uint64_t *boot_pml4 = (uint64_t *)vmm_get_boot_cr3();
    if (boot_pml4[0] & VMM_PRESENT) {
        uint64_t *pdpt = (uint64_t *)(boot_pml4[0] & ~0xFFFULL);
        /* Bochs VGA LFB is 16MB; mark 8 x 2MB huge pages as uncacheable */
        for (uint64_t addr = bar0; addr < bar0 + 16 * 1024 * 1024; addr += 0x200000) {
            int pi = (addr >> 30) & 0x1FF;
            if (!(pdpt[pi] & VMM_PRESENT)) continue;
            uint64_t *pd = (uint64_t *)(pdpt[pi] & ~0xFFFULL);
            int di = (addr >> 21) & 0x1FF;
            if (pd[di] & VMM_PRESENT)
                pd[di] |= VMM_PCD;
        }
        /* Flush TLB so the cache attribute change takes effect */
        __asm__ volatile (
            "mov %%cr3, %%rax\n\t"
            "mov %%rax, %%cr3"
            ::: "rax", "memory"
        );
    }

    available = true;
    kprintf("[FB] Bochs VGA at 0x%llx (VBE ID=0x%04x)\n", bar0, id);
}

bool fb_is_available(void) {
    return available;
}

int fb_set_mode(uint32_t width, uint32_t height) {
    if (!available) return -1;

    /* Disable VBE first */
    vbe_write(VBE_DISPI_INDEX_ENABLE, VBE_DISPI_DISABLED);

    /* Set resolution and BPP */
    vbe_write(VBE_DISPI_INDEX_XRES, (uint16_t)width);
    vbe_write(VBE_DISPI_INDEX_YRES, (uint16_t)height);
    vbe_write(VBE_DISPI_INDEX_BPP, 32);

    /* Set virtual width to match physical width */
    vbe_write(VBE_DISPI_INDEX_VIRT_W, (uint16_t)width);

    /* Enable with LFB */
    vbe_write(VBE_DISPI_INDEX_ENABLE, VBE_DISPI_ENABLED | VBE_DISPI_LFB_ENABLED);

    /* Read back actual virtual width (hardware may round up) */
    uint16_t actual_vw = vbe_read(VBE_DISPI_INDEX_VIRT_W);
    fb_width = width;
    fb_height = height;
    fb_bpp = 32;
    fb_pitch = (uint32_t)actual_vw * 4;

    fb_clear(FB_BLACK);

    kprintf("[FB] Mode set: %ux%ux%u\n", width, height, fb_bpp);
    return 0;
}

void fb_text_mode(void) {
    if (!available) return;
    vbe_write(VBE_DISPI_INDEX_ENABLE, VBE_DISPI_DISABLED);
    fb_width = 0;
    fb_height = 0;
}

void fb_get_info(struct fb_info *info) {
    info->width = fb_width;
    info->height = fb_height;
    info->pitch = fb_pitch;
    info->bpp = fb_bpp;
    info->framebuffer = (uint64_t)lfb;
}

void fb_putpixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= fb_width || y >= fb_height) return;
    uint32_t stride = fb_pitch / 4; /* stride in pixels */
    lfb[y * stride + x] = color;
}

void fb_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    uint32_t stride = fb_pitch / 4;
    for (uint32_t j = y; j < y + h && j < fb_height; j++) {
        for (uint32_t i = x; i < x + w && i < fb_width; i++) {
            lfb[j * stride + i] = color;
        }
    }
}

void fb_clear(uint32_t color) {
    uint32_t stride = fb_pitch / 4;
    for (uint32_t j = 0; j < fb_height; j++)
        for (uint32_t i = 0; i < stride; i++)
            lfb[j * stride + i] = color;
}

void fb_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = fb_font8x16[c - 32];

    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_putpixel(x + col, y + row, color);
        }
    }
}

void fb_draw_string(uint32_t x, uint32_t y, const char *s, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    while (*s) {
        if (*s == '\n') {
            cx = x;
            y += 16;
        } else {
            fb_draw_char(cx, y, *s, fg, bg);
            cx += 8;
        }
        s++;
    }
}
